package Week1;

public class VariablesAndOperations {

	public static void main(String[] args) {
		
		int availableSeatsOnFlight = 8;
		double costOfGroceriesAtCheckout = 109.33;
		char middleInitial = 'M';
		boolean isHotOutside = true;
		String customerFirstName = "Felicia";
		String streetAddress = "124 Main St";
		
		System.out.println("Plane Seats Avavailable: " + availableSeatsOnFlight);
		System.out.println("Grocery Total: " + costOfGroceriesAtCheckout);
		System.out.println("Middle Initial: " + middleInitial);
		System.out.println("It is hot outside: " + isHotOutside);
		System.out.println("Customer First Name: " + customerFirstName);
		System.out.println("Street Address: " + streetAddress);
	}
}
